<p>
    <b>Brothers Grimm</b><br><br>The Brothers Grimm
    (German: die Brüder Grimm or die Gebrüder Grimm),
    Jacob (1785–1863) and Wilhelm (1786–1859),
    were German academics who together collected and
    published folklore. The brothers are among the
    best-known storytellers of folktales, popularizing
    stories such as "Cinderella" ("Aschenputtel"),
    "The Frog Prince" ("Der Froschkönig"),
    "Hansel and Gretel" ("Hänsel und Gretel"),
    "Town Musicians of Bremen" ("Die Bremer Stadtmusikanten"),
    "Little Red Riding Hood" ("Rotkäppchen"), "Rapunzel",
    "Rumpelstiltskin" ("Rumpelstilzchen"), "Sleeping Beauty"
    ("Dornröschen"), and "Snow White" ("Schneewittchen").
    Their first collection of folktales, Children's and
    Household Tales (Kinder- und Hausmärchen), w
    as first published in 1812.
</p>
<p>

    <u>Brothers Grimm</u><br>The Brothers Grimm spent their
    formative years in the town of Hanau in the Landgraviate
    of Hesse-Kassel. Their father's death in 1796
    (when Jacob was 11 and Wilhelm 10) caused great poverty
    for the family and greatly affected the brothers
    throughout their lives. Both brothers attended Marburg
    University, where they developed a curiosity about
    German folklore, which grew into a lifelong dedication
    to collecting German folktales.
</p>
<p>
    <i>Brothers Grimm</i>The rise of Romanticism in 19th-century
    Europe revived interest in traditional folk stories,
    which to the Brothers Grimm represented a pure form
    of national literature and culture. With the goal of
    researching a scholarly treatise on folktales, they
    established a methodology for collecting and recording
    folk stories that became the basis for folklore studies.
    Between 1812 and 1857 their first collection was revised
    and republished many times, growing from 86 stories to
    more than 200. In addition to writing and modifying
    folktales, the brothers wrote collections of well-respected
    Germanic and Scandinavian mythologies, and in 1838 they
    began writing a definitive German dictionary
    (Deutsches Wörterbuch), which they were unable to finish.
</p>
